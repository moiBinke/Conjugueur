    private void conjuguerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conjuguerActionPerformed
        // TODO add your handling code here:
        //On reinitialise les combobox des groupes:
        groupe1.setSelectedIndex(0);
        groupe2.setSelectedIndex(0);
        groupe3.setSelectedIndex(0);
        //
        final Charset UTF_8=Charset.forName("UTF-8");
	final Charset ISO=Charset.forName("ISO-8859-1");
       
       //
       String conj_irr=new String("");
        Verbe verbe;
        verbe=new Verbe(entrerverbe.getText());
        if(verbe!=null){
            //
            
            if(verbe.est_pronominal()){
                verbe.enlever();
            }
            //Si le verbe existe dans la base de donnees
            verbe.connecter();
            if(verbe.verbe_exist()){
                  
                //si le verbe est irregulier
                if(verbe.est_irregulier()){
                    //****
                        present.setText("");
                        passecompose.setText("");
                        imparfait.setText("");    
                        plusqueparfait.setText("");
                        passesimple.setText("");    
                        passeanterieur.setText("");       
                        futursimple.setText("");      
                        futuranterieur.setText("");       
                    ResultSet irregulier;
                    try{
                    irregulier=connexion.SQLSelect("Select * from verbe_irregulier where Lower(verbes) like '"+verbe.getVerbe()+"'");
                    while(irregulier.next()){
                        present.append(irregulier.getString(3));
                        passecompose.append(irregulier.getString(4));
                        imparfait.append(irregulier.getString(5));     
                        plusqueparfait.append(irregulier.getString(6));
                        passesimple.append(irregulier.getString(7));        
                        passeanterieur.append(irregulier.getString(8));        
                        futursimple.append(irregulier.getString(9));        
                        futuranterieur.append(irregulier.getString(10));        
                    }
                    }
                    catch(SQLException e){
                         JOptionPane jop1;
                         jop1=new JOptionPane();
                         jop1.showMessageDialog(null,"Votre verbe n'est pas pris en compte par notre application");
                    }
                }
                //si le verbe est regulier
                else{
                  
                    verbe.conjuguer_regulier(present,passecompose,imparfait,plusqueparfait,passesimple,passeanterieur,futursimple,futuranterieur);
                }
               Fichier fichier =new Fichier("conjugaison//a_reviser.txt");
               conj_irr=verbe.getVerbe();
               fichier.ecrire(conj_irr+"\n");
               fichier.fermer_fichier();
            }
            //Si le verbe n,existe pas dans la base de donnees
            else{
                JOptionPane jop1;
                 jop1=new JOptionPane();
                 jop1.showMessageDialog(null,"Votre verbe n'est pas pris en compte par notre application");
            }
        }
        else{
            present.append("Le contenu est a null");
        }
    }//GEN-LAST:event_conjuguerActionPerformed


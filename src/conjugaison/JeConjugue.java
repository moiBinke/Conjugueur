    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        entrerverbe.setFont(new Font("Arial Unicode MS", Font.PLAIN,12));
       
        connexion.connect();
        ResultSet group1;
        ResultSet group2;
        ResultSet group3;
        try{
        group1=connexion.SQLSelect("Select verbes from groupe1");
        while(group1.next()){
            groupe1.addItem(group1.getString(1));
        }
        group2=connexion.SQLSelect("Select verbes from groupe2");
        while(group2.next()){
            groupe2.addItem(group2.getString(1));
        }
        group3=connexion.SQLSelect("Select distinct verbes from verbe_irregulier");
        while(group3.next()){
            groupe3.addItem(group3.getString(1));
        }
        }
        catch(SQLException e){
            System.out.println("Impossible d'executer une des requetes");
        }
    }//GEN-LAST:event_formWindowOpened


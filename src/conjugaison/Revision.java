    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         
        if(iterateur>0){
           iterateur--;
           ImageIcon  icone= new ImageIcon("/conjugaison/revision/cours_de_base/"+cours[iterateur]);
           cours_conjugaison.setIcon(icone);
            NoPage.setText(""+(iterateur+1)+"/"+cours.length);
         }
        else{
             iterateur=cours.length-1;
              NoPage.setText(""+(iterateur+1)+"/"+cours.length);
             ImageIcon  icone= new ImageIcon("/conjugaison/revision/cours_de_base/"+cours[iterateur]);
             cours_conjugaison.setIcon(icone);
         }
    }//GEN-LAST:event_jButton3ActionPerformed


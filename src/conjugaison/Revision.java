    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
       
       if(iterateur<cours.length){
           
           ImageIcon  icone= new ImageIcon("conjugaison//revision//cours_de_base//"+cours[iterateur]);
           cours_conjugaison.setIcon(icone); 
           NoPage.setText(""+(iterateur+1)+"/"+cours.length);
           iterateur++;
       }
       else{
           iterateur=0;
           ImageIcon  icone=  new ImageIcon("conjugaison//revision//cours_de_base//"+cours[iterateur]);
           cours_conjugaison.setIcon(icone); 
           NoPage.setText(""+(iterateur+1)+"/"+cours.length);
            iterateur++;
       }
    }//GEN-LAST:event_jButton10ActionPerformed

